let mes = prompt("Informe o mês do ano");

switch (mes.toLowerCase()){
        case "janeiro":
           console.log("Ano novo");
           break;
        case "fevereiro":
           console.log("Carnaval");
           break;
        case "março":
           console.log("Dia da mulher");
           break;
        case "abril":
            console.log("Pascoa");
            break;
        case "maio":
            console.log("Dia das mães");
            break;
        case "junho":
           console.log("Quadrilha");
           break;
        case "julho":
           console.log("Férias");
           break;
        case "agosto":
           console.log("Dia dos pais");
           break;
        case "setembro":
           console.log("Independência do Brasil");
           break;
        case "outubro":
           console.log("Dia das crianças");
           break;
        case "novembro":
           console.log("Dia dos finados");
           break;
        case "dezembro":
           console.log("Natal");
           break;                
}